import time
n1 = float(15)
n2 = float(4)

# Adição
print("Adição:", n1 + n2)

#Subtração
print("Subtração:", n1 - n2)

#Multiplicação
print("Multiplicação:", n1 * n2)

#Divisão 
print("Divisão:", n1 / n2)

#Divisão Inteira
print("Divisão Inteira:", n1 // n2)

#Resto de Divisão
print("Resto de Divisão:", n1 % n2)

#Potência
print("Potência:",n1 ** n2)


time.sleep(5)